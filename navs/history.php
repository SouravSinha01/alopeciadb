        <h2>Early 20th Century</h2>
        <p>In the 1930s, scientists discovered the link between hormones and hair loss, identifying dihydrotestosterone (DHT) as a key factor in male pattern baldness, which shaped the modern understanding of androgenic alopecia. By the 1940s, research highlighted the hereditary nature of the condition, recognizing that both men and women could experience hair loss due to genetic and hormonal influences, with hair loss patterns being specific and progressive, especially in men</p>
