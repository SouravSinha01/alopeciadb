        <h2>Mid 20th Century: Treatment developments</h2>
        <p><p>
  <strong>1950s-1960s:</strong> The rise of pharmaceutical companies led to new treatments, including the development of minoxidil. Initially an oral medication for high blood pressure, it was later found to promote hair growth and marketed as a topical solution for hair loss in the 1980s.
</p>

<p>
  <strong>1970s:</strong> Research on the connection between DHT and androgenic alopecia led to the discovery of finasteride, a drug that inhibits the conversion of testosterone to DHT, targeting the hormonal causes of AGA.
</p>
<p>
  <strong>1997:</strong> The FDA approved <strong>minoxidil</strong> as the first non-prescription treatment for androgenic alopecia in both men and women. Finasteride, marketed as Propecia, was also approved for men, enhancing treatment options.
</p>

<p>
  <strong>Late 20th Century:</strong> The 1990s saw the advent of surgical treatments like hair transplantation. Techniques like follicular unit transplantation (FUT) made the procedure more effective and less noticeable.
</p>


</p>
