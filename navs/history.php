      <h1>About</h1>
      <p>Androgenic alopecia (AGA), commonly known as male or female pattern baldness, is a genetic condition that leads to hair loss, primarily affecting the scalp. It is influenced by both genetic and hormonal factors, typically involving the hormone dihydrotestosterone (DHT), which causes hair follicles to shrink and eventually stop producing hair.</p>
      <p> The history of androgenic alopecia involves both medical understanding and the development of treatments over centuries..</p>
